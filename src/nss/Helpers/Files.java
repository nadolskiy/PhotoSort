public class Files {
    /*******************************************************************************************************************
     * Отримуємо розширення файлу ************************************************************************************ *
     * *************************************************************************************************************** *
     * @param fullPathToImage шлях до файлу ************************************************************************** *
     * @return розширення файлу             ************************************************************************** *
     ******************************************************************************************************************/
    public static String getFileType(String fullPathToImage) {
        String[] arrPath = fullPathToImage.split("\\.");                        // розбиваємо шлях по "."
        return arrPath[(arrPath.length - 1)];                                   // вертаємо крайній правий елемент (тип)
    }
}