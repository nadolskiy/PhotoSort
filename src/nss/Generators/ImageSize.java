public class ImageSize {
    /*******************************************************************************************************************
     * Приведення розміру зображення до необхідного формату ********************************************************** *
     * *************************************************************************************************************** *
     * @param imageWidth  ширина зображення        ******************************************************************* *
     * @param imageHeight висота зображення        ******************************************************************* *
     * @return розмір зображення у потрібній формі ******************************************************************* *
     ******************************************************************************************************************/
    public static String getSize(Object imageWidth, Object imageHeight) {
        // якщо ширина або висота зображення - 0
        if ((int)imageWidth == 0 || (int)imageHeight == 0)
            return "";                                                                   // вертаємо порожній розмір
        // інакше (якщо розмір більший за 0)
        else
            return "(" + imageWidth + "x" + imageHeight + ")";                           // вертаємо нову форму розміру
    }
}