public class CameraInfo {
    /*******************************************************************************************************************
     * Отримуємо марку та модель камери ****************************************************************************** *
     * *************************************************************************************************************** *
     * @param exifIFDOD ExifIFD0Directory            ***************************************************************** *
     * @return масив з назвою марки та моделі камери ***************************************************************** *
     *******************************************************************************************************************/
    public static String[] getCameraNameAndModel(ExifIFD0Directory exifIFDOD) {
        // якщо є мета-дані
        if (exifIFDOD != null) {
            return new String[]{                                                            // вертаємо масив з
                    exifIFDOD.getString(ExifIFD0Directory.TAG_MAKE),                        // маркою камери
                    exifIFDOD.getString(ExifIFD0Directory.TAG_MODEL)};                      // та моделлю камери
        // інакше (якщо мета-дані відсутні)
        } else {
            return new String[]{                                                            // вертаємо масив з
                    Values.NO_CAMERA_NAME,                                                  // заглушку для марки
                    Values.NO_CAMERA_MODEL};                                                // та заглушку для моделі
        }
    }
}